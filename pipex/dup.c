#include "../minishell.h"

void	ft_dup2(int **fd, int i, int num_cmds)
{
	if (i > 0)
		dup2(fd[i - 1][0], STDIN_FILENO);
	if (i < num_cmds - 1)
		dup2(fd[i][1], STDOUT_FILENO);
}
